-- POLÍTICAS: Atleta acessa apenas seus próprios dados
DO $$ 
DECLARE 
  t text;
BEGIN
  FOR t IN SELECT table_name 
           FROM information_schema.tables 
           WHERE table_schema = 'public' 
           AND table_name IN ('anamnese', 'bem_estar', 'ciclo_menstrual', 'sintomas_menstruais', 'prontidao', 'comorbidades', 'registros_clinicos', 'treinos', 'sessoes_treino', 'avaliacoes')
  LOOP
    EXECUTE format('DROP POLICY IF EXISTS "Atletas acessam seus próprios dados" ON %I', t);
    EXECUTE format('CREATE POLICY "Atletas acessam seus próprios dados" ON %I FOR ALL USING (auth.uid() = athlete_id)', t);
  END LOOP;
END $$;

